var new_order_nivel_4; //nuevo orden del array de la fase uno
var primeraToma; //tiempo de inicio de casa item de la fase uno
var user_id; // id del usuario
var position = 0;
/*
 * 
 * @type type
 */
class random_nivel_4{
    /*
     * Hacer aleatorio los items de la fase uno
     * @returns {Generator}
     */
    order_nivel_4(){
        position = 0;
        var new_order = nivel_4.sort(function() {return Math.random() - 0.5});
        for(var k in new_order){
            document.getElementById('menu'+k+'_color').src = '../../pix/Imagenes/'+new_order[k]['img']+'.png';
            document.getElementById('menu'+k+'_audio').src = '../../pix/Sonidos/'+new_order[k]['son']+'.wav';
            document.getElementById('img_btn_'+k+'_1').src = '../../pix/animales/'+new_order[k]['opt1']+'.png';
            document.getElementById('img_btn_'+k+'_2').src = '../../pix/animales/'+new_order[k]['opt2']+'.png';
            primeraToma = Date.now();
        }
        new_order_nivel_4 = new_order;
        var url_string = window.location.href; 
        var url = new URL(url_string);
        user_id = url.searchParams.get("id_user");
    };
    /*
     * Guardar in array la respuesta y el tiempo, pasar al sigiente item y en el ultimo item enviar a guardar
     * @param {int} key
     * @param {int} opt
     * @returns {Generator}
     */
    onclik_respose(key, opt){
        new_order_nivel_4[key]['res_user'] = new_order_nivel_4[key]['opt'+opt];
        new_order_nivel_4[key]['time'] = rn4.medir_tiempo();
        var ultimo = (new_order_nivel_4.length)-1;
        document.getElementById('menu'+key+'_audio').pause();
        if(key < ultimo){
            position = key+1;
            if(key < 3){
                if(new_order_nivel_4[key]['res_user'] == new_order_nivel_4[key]['res']){
                    document.getElementById('menu'+key+'_color').innerHTML = '<i class="fa fa-fw fa-check"></i>';
                }else{
                    document.getElementById('menu'+key+'_color').innerHTML = '<i class="fa fa-fw fa-close"></i>';
                }
            }
            document.getElementById("tab_"+(key+1)).click();
            document.getElementById("menu"+(key+1)+'_audio').play();
            primeraToma = Date.now();
        }else{
            rn4.ajax_nivel_4();
        }
    };
    /**
     * Medir el tiempo en segundo de la respuesta
     * @returns {Number} secons
     */
    medir_tiempo(){
        var segundaToma = Date.now();
        return (segundaToma-primeraToma)/1000;
    }
    /*
     * Guardar las respuestas de la fase uno
     * @returns {Generator}
     */
    ajax_nivel_4(){
        document.getElementById('resumen_nivel_4').innerHTML = "";
        var rv = {};
        for (var i = 0; i < new_order_nivel_4.length; ++i)
            rv[i] = Object.assign({}, new_order_nivel_4[i]);
        
        $.ajax({
            url : '../../methods/save_levels.php',
            data : {action: 4,user: user_id, data:JSON.stringify(rv), letra: 0},
            type : 'POST',
            success : function(json) {
                
            },
            error : function(result, textStatus, errorThrown) {
                
            },
            complete : function(json, status) {
                if(status != 'error'){                  
                    try {
                        var fjson = JSON.parse(json.responseText);
                        
                    } catch (e) {
                        console.log('la guardada se petaquió')
                    } finally {
                        document.getElementById('resumen_nivel_4').innerHTML ='Fin de la prueba de Imágenes<br>';
                        document.getElementById('resumen_nivel_4').innerHTML +='<button type="button" class="button_ir" onclick="location.href = \'resumen_all_levels.php?id_user='+user_id+'\'">Continuar</button>';
                        document.getElementById('resumen_nivel_4').style.backgroundImage = "url(../../pix/Imagenes/nivel_1.png)";
                        document.getElementById('resumen_nivel_4').style.backgroundSize = "65%";
                        document.getElementById('resumen_nivel_4').style.backgroundRepeat  = "no-repeat";
                        document.getElementById('resumen_nivel_4').style.backgroundPosition = "top";
                        document.getElementById('resumen_nivel_4').style.position = 'absolute';
                    }  
                }
            }
        });
    }
    compruebaTecla(evento){
        var keyCode = document.layers ? evento.which : document.all ? event.keyCode : document.getElementById ? evento.keyCode : 0;
        if (keyCode == 39){ //alert("flecha derecha");
            document.getElementById('menu'+position+'_opt2').click();
        }else if (keyCode == 37){//alert("flecha izquierda");
            document.getElementById('menu'+position+'_opt1').click();
        }
        return true;
    }
}
var rn4 = new random_nivel_4;
rn4.order_nivel_4();
document.onkeydown = rn4.compruebaTecla;
